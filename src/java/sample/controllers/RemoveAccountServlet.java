package sample.controllers;

import java.io.IOException;
import java.sql.SQLException;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.Cookie;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;
import sample.dao.UserDAO;
import sample.dto.UserDTO;

/**
 *
 * @author haong
 */
@WebServlet(name = "removeAccountServlet", urlPatterns = {"/removeAccountServlet"})
public class RemoveAccountServlet extends HttpServlet {

    private final String ERROR_PAGE = "errorRegistration.html";
    private final String REMOVE_SUCCESS = "removeAccount.jsp";

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        String url = ERROR_PAGE;
        try {
            HttpSession session = request.getSession(false);
            if (session.getAttribute("user") != null) {
                UserDTO dto = (UserDTO) session.getAttribute("user");
                UserDAO dao = new UserDAO();
                boolean result = dao.removeAccount(dto.getUserName());
                if (result) {
                    url = REMOVE_SUCCESS;
                    session.invalidate();
                    Cookie[] c = request.getCookies();
                    String token = "";
                    if (c != null) {
                        for (Cookie aCookie : c) {
                            if (aCookie.getName().equals("selector")) {
                                token = aCookie.getValue();
                            }
                        }
                    }
                    if (!token.equals("")) {
                        UserDTO userDTO = UserDAO.getToken(token);
                        if (userDTO != null) {
                            Cookie resetCookie = new Cookie("selector", "0");
                            resetCookie.setMaxAge(0);
                            response.addCookie(resetCookie);
                        }
                    }
                }
            }
        } catch (ClassNotFoundException ex) {
            log("removeAccountServlet_ClassNotFoundException" + ex.getMessage());
        } catch (SQLException ex) {
            log("removeAccountServlet_SQLException" + ex.getMessage());
        } finally {
            response.sendRedirect(url);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
